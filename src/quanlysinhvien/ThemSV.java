    private void btnTSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTSVActionPerformed
        // TODO add your handling code here:
        try {
            ClsDatabase.open();
            Statement s = ClsDatabase.con.createStatement();
            ResultSet rs = s.executeQuery("select * from sinhvien where mssv = '" + txtMSSV.getText() + "'");
            rs.next();
            txtHten.setText(rs.getString(2));
            txtNsinh.setText(rs.getString(3));
            radnam.setSelected(rs.getBoolean(4));
            txtTenLop.setText(rs.getString(5));
            txtMaLop.setText(rs.getString(6));
            txtKhoaS.setText(rs.getString(7));
            txtKhoa.setText(rs.getString(8));
            txtMk.setText(rs.getString(11));
            ClsDatabase.close();
            txtMSSV.enable(false);
            btnsua.setEnabled(true);
            btnThem.setEnabled(false);
            btnTSV.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Không tìm Thấy sinh viên");
        }
    }//GEN-LAST:event_btnTSVActionPerformed

