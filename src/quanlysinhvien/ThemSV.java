    public boolean ktra(String s) {
        if (s.matches("[A-Z][0-9]{7}")) {
            try {
                ClsDatabase.open();
                Statement sk = ClsDatabase.con.createStatement();
                ResultSet rs = sk.executeQuery("select * from sinhvien where mssv = '" + s + "'");
                if (rs.next()) {
                    return false;
                }
                ClsDatabase.close();
            } catch (Exception e) {

            }
        } else {
            return false;
        }
        return true;
    }

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (ktra(txtMSSV.getText())) {
            try {
                ClsDatabase.open();
                String sql = "insert into sinhvien(mssv,hten,ngaysinh,gtinh,lop,mlop,khoah,khoa,mk) values (?,?,?,?,?,?,?,?,?)";
                PreparedStatement ps = ClsDatabase.con.prepareStatement(sql);
                ps.setString(1, txtMSSV.getText());
                ps.setString(2, txtHten.getText());
                ps.setString(3, txtNsinh.getText());
                ps.setBoolean(4, radnam.isSelected());
                ps.setString(5, txtTenLop.getText());
                ps.setString(6, txtMaLop.getText());
                ps.setString(7, txtKhoaS.getText());
                ps.setString(8, txtKhoa.getText());
                ps.setString(9, txtMk.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Thêm Thành công");
                ClsDatabase.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Thêm Sinh Viên Không thành công\nyêu cầu kiểm tra thông tin");
                e.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"MSSV đã tồn tại\nHoặc sai định dạng");
        }
    }//GEN-LAST:event_btnThemActionPerformed

