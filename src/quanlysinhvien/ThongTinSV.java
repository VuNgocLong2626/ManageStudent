    public float Doi(String s) {
        if (s.equals("A")) {
            return 4f;
        }
        if (s.equals("B+")) {
            return 3.5f;
        }
        if (s.equals("B")) {
            return 3f;
        }
        if (s.equals("C+")) {
            return 2.5f;
        }
        if (s.equals("C")) {
            return 2f;
        }
        if (s.equals("D+")) {
            return 1.5f;
        }
        if (s.equals("D")) {
            return 1;
        }
        return 0;
    }
