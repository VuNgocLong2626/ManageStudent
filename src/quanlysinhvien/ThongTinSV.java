    private void btnLietKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLietKeActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jtshow.getModel();
        tblModel.setRowCount(0);
        try {
            float TL = 0, DiemSS = 0, DK = 0;

            ClsDatabase.open();
            Statement s = ClsDatabase.con.createStatement();
            String sql ="select distinct m.mmon,m.tenmon,m.sotinchi,g.mnhom,d.so,d.chu from sinhvien s join diem d on s.mssv = d.mssv join giangday g on g.mnhom = d.mnhom join monhoc m on m.mmon = g.mmon where g.namhoc = '"+cboNamHoc.getSelectedItem().toString()+"' and g.kyhoc ='"+cboKy.getSelectedItem().toString()+"' and s.mssv = '"+getMssv()+"'" ; 
            
            if (cboNamHoc.getSelectedItem().toString().equals("ALL") && cboKy.getSelectedItem().toString().equals("ALL")) {
                sql = "select m.mmon,m.tenmon,m.sotinchi,g.mnhom,d.so,d.chu from sinhvien s join diem d on s.mssv = d.mssv join giangday g on g.mnhom = d.mnhom join monhoc m on m.mmon = g.mmon where s.mssv = '" + this.getMssv() + "'";
            } else {
                if (cboKy.getSelectedItem().toString().equals("ALL")) {
                    sql = "select distinct m.mmon,m.tenmon,m.sotinchi,g.mnhom,d.so,d.chu from sinhvien s join diem d on s.mssv = d.mssv join giangday g on g.mnhom = d.mnhom join monhoc m on m.mmon = g.mmon where g.namhoc = '"+cboNamHoc.getSelectedItem().toString()+"' and s.mssv = '"+getMssv()+"'" ;
                }
            }

            ResultSet rs = s.executeQuery(sql);
            while (rs.next()) {
                String MaHP = rs.getString(1);
                String TenHP = rs.getString(2);
                String Stc = rs.getString(3);
                String Mnhom = rs.getString(4);
                String so = rs.getString(5);
                String chu = rs.getString(6);
                DK += Float.parseFloat(Stc);
                if (!chu.equals("") && !chu.equals("F")) {
                    TL += Float.parseFloat(Stc);
                    DiemSS += Doi(chu) * Float.parseFloat(Stc);
                }
                String Data[] = {MaHP, TenHP, Stc, Mnhom, so, chu};

                tblModel.addRow(Data);
            }
            jbTcDK.setText(String.valueOf(DK));
            jbDiemTB.setText(String.valueOf((float) (DiemSS/TL)));
            jbHT.setText(String.valueOf(TL));
            ClsDatabase.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLietKeActionPerformed

