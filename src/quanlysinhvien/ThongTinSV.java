    public void ShowNamHoc() {
        try {
            ClsDatabase.open();
            Statement s = ClsDatabase.con.createStatement();
            ResultSet rs = s.executeQuery("select distinct g.namhoc from sinhvien s join diem d on s.mssv = d.mssv join giangday g on g.mnhom = d.mnhom where s.mssv = '" + this.getMssv() + "'");
            while (rs.next()) {
                String namhoc = rs.getString(1);
                cboNamHoc.addItem(namhoc);
            }
            cboNamHoc.addItem("ALL");
            ClsDatabase.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

