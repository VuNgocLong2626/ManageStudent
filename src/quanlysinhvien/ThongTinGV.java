    public boolean checkDiem(String k) {
        try {
            Float s = Float.parseFloat(k);
            if (s >= 0 && s <= 10) {
                return true;
            } else {
                return false;
            }
        } catch (Exception e) {

        }
        return false;
    }
    private void btnCNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCNActionPerformed
        if (checkDiem(txtDiem.getText())) {
            try {
                ClsDatabase.open();
                String url = "update diem set so = ?,chu = ? where mssv = ? and mnhom = ? ";
                PreparedStatement ps = ClsDatabase.con.prepareStatement(url);
                ps.setFloat(1, Float.parseFloat(txtDiem.getText()));
                ps.setString(2, this.Doi(Float.parseFloat(txtDiem.getText())));
                ps.setString(3, txtMssv.getText());
                ps.setString(4, cboLop.getSelectedItem().toString());
                ps.executeUpdate();
                System.out.println("OK");
                ClsDatabase.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
            this.ShowDSLop();
        } else {
            JOptionPane.showMessageDialog(this, "Điểm nhập không đúng");
        }
    }//GEN-LAST:event_btnCNActionPerformed

    public String Doi(float i) {
        String Dchu = "F";
        if (i >= 9) {
            Dchu = "A";
        } else {
            if (i >= 8) {
                Dchu = "B+";
            } else {
                if (i >= 7) {
                    Dchu = "B";
                } else {
                    if (i >= 6.5) {
                        Dchu = "C+";
                    } else {
                        if (i >= 5.5) {
                            Dchu = "C";
                        } else {
                            if (i >= 4) {
                                Dchu = "D";
                            }
                        }
                    }
                }
            }
        }

        return Dchu;
    }
