    public void ShowDSLop() {
        DefaultTableModel tblModel = (DefaultTableModel) jtDs.getModel();
        tblModel.setRowCount(0);//reset view jtable
        try {
            ClsDatabase.open();
            Statement s = ClsDatabase.con.createStatement();
            ResultSet rs = s.executeQuery("select distinct s.hten,s.mssv,d.so from giaovien g join giangday gd on g.mcb = gd.mcb join diem d on d.mnhom = gd.mnhom join sinhvien s on s.mssv = d.mssv where g.mcb = '" + this.MCB + "' and gd.mnhom ='" + cboLop.getSelectedItem().toString() + "'");
            while (rs.next()) {
                String Hten = rs.getString(1);
                String mssv = rs.getString(2);
                String diemS = rs.getString(3);

                String Data[] = {Hten, mssv, diemS};

                tblModel.addRow(Data);
            }
            ClsDatabase.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
